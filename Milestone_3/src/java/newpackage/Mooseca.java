public class Mooseca {

    private int codice;
    private String nomeOgg;
    private String tipo;
    private String descrizione;
    private int quantità;
    private int prezzo;
    private int idVend;

    /**
     * @return the codice
     */
    public int getCodice() {
        return codice;
    }

    /**
     * @param codice the codice to set
     */
    public void setCodice(int codice) {
        this.codice = codice;
    }

    /**
     * @return the nomeOgg
     */
    public String getNomeOgg() {
        return nomeOgg;
    }

    /**
     * @param nomeOgg the nomeOgg to set
     */
    public void setNomeOgg(String nomeOgg) {
        this.nomeOgg = nomeOgg;
    }

    /**
     * @return the tipo
     */
    public String getTipo() {
        return tipo;
    }

    /**
     * @param tipo the tipo to set
     */
    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    /**
     * @return the descrizione
     */
    public String getDescrizione() {
        return descrizione;
    }

    /**
     * @param descrizione the descrizione to set
     */
    public void setDescrizione(String descrizione) {
        this.descrizione = descrizione;
    }

    /**
     * @return the quantità
     */
    public int getQuantità() {
        return quantità;
    }

    /**
     * @param quantità the quantità to set
     */
    public void setQuantità(int quantità) {
        this.quantità = quantità;
    }

    /**
     * @return the prezzo
     */
    public int getPrezzo() {
        return prezzo;
    }

    /**
     * @param prezzo the prezzo to set
     */
    public void setPrezzo(int prezzo) {
        this.prezzo = prezzo;
    }

    /**
     * @return the idVend
     */
    public int getIdVend() {
        return idVend;
    }

    /**
     * @param idVend the idVend to set
     */
    public void setIdVend(int idVend) {
        this.idVend = idVend;
    }

}
